const buddyMessages = {
  'en-US': enUS,
  'zh-CN': zhCN,
} satisfies Record<BuddyLocale, Record<BuddyI18nKey, string>>

export type BuddyTranslate = (
  key: BuddyI18nKey,
  params?: Record<string, string | number>,
) => string

export function resolveBuddyLocale(language: string): BuddyLocale {
  const normalizedLanguage = language.trim().toLowerCase()
  if (normalizedLanguage === 'en' || normalizedLanguage.startsWith('en-'))
    return 'en-US'

  return 'zh-CN'
}

export function translateBuddy(
  locale: BuddyLocale,
  key: BuddyI18nKey,
  params: Record<string, string | number> = {},
) {
  return formatBuddyMessage(buddyMessages[locale][key], params)
}

export function useBuddyI18n(language: MaybeRefOrGetter<string>) {
  const locale = computed(() => resolveBuddyLocale(toValue(language)))
  const languageOptions = computed(() => [
    {
      label: '中文',
      value: 'zh-CN',
    },
    {
      label: 'English',
      value: 'en-US',
    },
  ])
  const t: BuddyTranslate = (key, params) => translateBuddy(locale.value, key, params)

  return {
    languageOptions,
    locale,
    t,
  }
}

function formatBuddyMessage(
  template: string,
  params: Record<string, string | number>,
) {
  return template.replace(/\{(\w+)\}/g, (match, key: string) => {
    const value = params[key]
    return value === undefined ? match : String(value)
  })
}
